/* Loops are control structures in programming that allow the repeated execution of a block of code as long as a certain condition is true. They are essential for automating repetitive tasks and iterating over collections of data. */


// There are several types of loops, and they serve different purposes. The main types of loops include:

// for loop
// while loop
// do-while loop


//for loop 
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// while loop
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}


// do while
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);




